/**
 * Clase que encuentra la posición.
 * @autor Jeremy Montero
 * @version 1.0
 */
public class Posicion {
    public int fila;
    public int columna;

    /**
     * Constructor de la clase Posición.
     * @param fila Valor de la fila.
     * @param columna Valor de la columna
     */
    public Posicion(int fila, int columna) {
        this.fila = fila;
        this.columna = columna;
    }
}
